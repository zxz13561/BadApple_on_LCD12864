void LCD_WRITE_COMMAND(uint8_t command_hex) {
  uint8_t _comm[3] = {
    0xF8,
    command_hex & 0xF0,
    (command_hex << 4) & 0xF0
  };
  LCD_SEND_BYTE(_comm);
}

void LCD_WRITE_CHAR(uint8_t char_hex) {
  uint8_t _comm[3] = {
    0xFA,
    char_hex & 0xF0,
    (char_hex << 4) & 0xF0
  };
  LCD_SEND_BYTE(_comm);
}

void DrawFullScreen(uint8_t *p)
{
  int ygroup,x,y,i;
  int temp;
  int tmp;
          
  for(ygroup=0;ygroup<64;ygroup++) {
    if(ygroup<32) {
      x=0x80;
      y=ygroup+0x80;
    }
    else {
      x=0x88;
      y=ygroup-32+0x80;    
    }         
    LCD_WRITE_COMMAND(0x36);      
    LCD_WRITE_COMMAND(y);
    LCD_WRITE_COMMAND(x);
    LCD_WRITE_COMMAND(0x32);
    tmp=ygroup*16;
    for(i=0;i<16;i++) {
      temp=p[tmp++];
      LCD_WRITE_CHAR(temp);
    }
  }
  LCD_WRITE_COMMAND(0x34);
  LCD_WRITE_COMMAND(0x36);
}

